// -------------------------------------------------------------------------------


// -------------------------------------------------------------------------------
// VertexBuffer class
// -------------------------------------------------------------------------------
class VertexBuffer
{
public:

	// -------------------------------------------------------------------------------
	// @brief	コンストラクタ
	// -------------------------------------------------------------------------------
	VertexBuffer();

	// -------------------------------------------------------------------------------
	// @brief	デストラクタ
	// -------------------------------------------------------------------------------
	~VertexBuffer();

	// -------------------------------------------------------------------------------
	// @brief	初期化処理を行う
	// 
	// @param[in]	pDevice		デバイス
	// @param[in]	size		頂点バッファサイズ
	// @param[in]	stride		1頂点あたりのサイズ
	// @param[in]	pInitData	初期化データ
	// @retval	true	初期化に成功
	// @retval	false	初期化に失敗
	// -------------------------------------------------------------------------------
	bool Init(ID3D12Device* _pDevice, size_t _size, size_t stride, const void* pInitData = nullptr);

	// -------------------------------------------------------------------------------
	// @brief	初期化処理を行う
	// 
	// @param[in]	pDevice		デバイス
	// @param[in]	size		頂点バッファサイズ
	// @param[in]	pInitData	初期化データ
	// @retval	true	初期化に成功
	// @retval	false	初期化に失敗
	// -------------------------------------------------------------------------------
	template<typename T>
	bool Init(ID3D12Device* _pDevice, size_t _size, const T* _pInitData = nullptr) 
	{ return Init(_pDevice, _size, sizeof(T), _pInitData); }

	// -------------------------------------------------------------------------------
	// @brief	終了処理を行う
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	メモリマッピングを行う
	// -------------------------------------------------------------------------------
	void* Map();

	// -------------------------------------------------------------------------------
	// @brief	メモリマッピングを解除
	// -------------------------------------------------------------------------------
	void Unmap();

	// -------------------------------------------------------------------------------
	// @brief	メモリマッピングを行う
	// -------------------------------------------------------------------------------
	template<typename T>
	T* Map() const 
	{ return reinterpret_cast<T*>(Map()); }

	// -------------------------------------------------------------------------------
	// @brief	頂点バッファビューを取得
	// 
	// @return	頂点バッファビューを返却
	// -------------------------------------------------------------------------------
	D3D12_VERTEX_BUFFER_VIEW GetView() const;

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	ComPtr<ID3D12Resource>		m_pVB;		// 頂点バッファ
	D3D12_VERTEX_BUFFER_VIEW	m_View;		// 頂点バッファビュー

	// -------------------------------------------------------------------------------
	// private methods
	// -------------------------------------------------------------------------------
	VertexBuffer	(const VertexBuffer&) = delete;
	void operator = (const VertexBuffer&) = delete;
};


