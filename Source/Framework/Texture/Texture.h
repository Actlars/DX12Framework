#pragma once

// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------
#include "../Pool/DescriptorPool/DescriptorPool.h"

// -------------------------------------------------------------------------------
// Texture class
// 
// 概要 : 
//		テクスチャ１枚分のGPUリソースとSRVハンドルを管理するクラス
//		DescriptorPoolからSRVハンドルを借り受け
//		デストラクタで自動的に返却する
// 
// 使い方 : 
//		Texture tex;
//		Tex.Init(_pDevice, _pQueue, _pPool, L"Assets/kerusi-.png");
//		CmdList->SetGraphicsRootDescriptorTable(1, tex.GetHandleGPU());
// -------------------------------------------------------------------------------
class Texture
{

public:

	// -------------------------------------------------------------------------------
	//	コンストラクタ
	// -------------------------------------------------------------------------------
	Texture() = default;

	// -------------------------------------------------------------------------------
	//	デストラクタ
	// -------------------------------------------------------------------------------
	~Texture() { Term(); }

	// -------------------------------------------------------------------------------
	// @brief	テクスチャを初期化（ファイルからロードしてGPUへアップロード）
	//
	// @param[in]	_pDevice	D3D12コマンド
	// @param[in]	_pQueue		コピー用コマンドキュー
	// @param[in]	_pPool		SRVを割り当てるDescriptorPool
	// @param[in]	_path		テクスチャファイルのパス
	// @retval	true	初期化成功
	// @retval	false	初期化失敗
	// -------------------------------------------------------------------------------
	bool Init(
		ID3D12Device*		_pDevice,
		ID3D12CommandQueue* _pQueue,
		DescriptorPool*		_pPool,
		const std::wstring& _path);


	// -------------------------------------------------------------------------------
	// @brief	終了処理。GPUリソースとSRVハンドルを開放する
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	シェーダーにバインドするためのGPUハンドルを返す
	// -------------------------------------------------------------------------------
	D3D12_GPU_DESCRIPTOR_HANDLE GetHandleGPU() const;

	// -------------------------------------------------------------------------------
	// @brief	CPUからSRVを書き込むためのCPUハンドルを返す
	// -------------------------------------------------------------------------------
	D3D12_CPU_DESCRIPTOR_HANDLE GetHandleCPU() const;

	// -------------------------------------------------------------------------------
	// @brief	テクスチャの横幅を返す
	// -------------------------------------------------------------------------------
	uint32_t GetWidth() const;

	// -------------------------------------------------------------------------------
	// @brief	テクスチャの縦幅を返す
	// -------------------------------------------------------------------------------
	uint32_t GetHeight() const;

	// -------------------------------------------------------------------------------
	// @brief	テクスチャのピクセルフォーマットを返す
	// -------------------------------------------------------------------------------
	DXGI_FORMAT GetFormat()const;

	// -------------------------------------------------------------------------------
	// @brief	GPUリソースを返す。（RenderTargetへのバインド等へ使用）
	// -------------------------------------------------------------------------------
	ID3D12Resource* GetResource()const;

private:

	// -------------------------------------------------------------------------------
	// @brief	ファイルパスの拡張子からDDSかどうか判定する
	// -------------------------------------------------------------------------------
	static bool IsDDS(const std::wstring& _path);

	// -------------------------------------------------------------------------------
	// @brief	CPUメモリ上の画像データをGPU（DEFAULTヒープ）へアップロードする
	// 
	// フロー
	// 1. DEFAULTヒープにGPUリソースを作成（コピー先）
	// 2. UPLOADヒープに中間バッファを作成
	// 3. ロード専用CommandListでUpdateSubresourcesを実行
	// 4. リソースバリアをCOPY_DEST → PIXEL_SHADER_RESOURCEに遷移
	// 5. フェンスで同期し、中間バッファの早期解放を防ぐ
	// 
	// @param[in]	_image	DirectXTexのScratchImage（CPUメモリ上のデータ）
	// @param[in]	_meta	テクスチャのメタ情報（サイズ、フォーマット等）
	// @param[out]	_outResource	アップロード先のGPUリソース
	// @retval	true	成功
	// @retval	false	失敗
	// -------------------------------------------------------------------------------
	bool UploadToGPU(
		const DirectX::ScratchImage&	_image,
		const DirectX::TexMetadata&		_meta,
		ComPtr<ID3D12Resource>&			_outResource);

	// -------------------------------------------------------------------------------
	// @brief	GPUリソースに対してSRVをDescriptorPoolに登録する
	// 
	// テクスチャの種類（通常2D / 配列 / キューブマップ）を自動判別してSRVを作成
	// 
	// @param[in]	_meta	テクスチャのメタ情報
	// -------------------------------------------------------------------------------
	void CreateSRV(const DirectX::TexMetadata& _meta);

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	ID3D12Device* m_pDevice			= nullptr;	// D3D12デバイス（所有権なし）
	ID3D12CommandQueue* m_pQueue	= nullptr;	// コマンドキュー（所有権なし）
	DescriptorPool* m_pPool			= nullptr;	// SRVプール（所有権なし）
	DescriptorHandle* m_pHandle		= nullptr;	// プールから借りたSRVハンドル

	ComPtr<ID3D12Resource>	m_pResource;		// GPUリソース本体

	uint32_t	m_Width		= 0;					// テクスチャ横幅
	uint32_t	m_Height	= 0;					// テクスチャ縦幅
	DXGI_FORMAT m_Format	= DXGI_FORMAT_UNKNOWN;	// ピクセルフォーマット

	// コピー禁止
	Texture			(const Texture&) = delete;
	void operator = (const Texture&) = delete;

};
