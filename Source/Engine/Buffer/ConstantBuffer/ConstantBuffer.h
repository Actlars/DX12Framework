#pragma once

// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------


// -------------------------------------------------------------------------------
// Forward Declarations
// -------------------------------------------------------------------------------
class DescriptorHandle;
class DescriptorPool;

// -------------------------------------------------------------------------------
// ConstantBuffer class
// -------------------------------------------------------------------------------
class ConstantBuffer
{
	// -------------------------------------------------------------------------------
	// list of friend classes and methods
	// -------------------------------------------------------------------------------
	/* NOTHING */

public:

	// -------------------------------------------------------------------------------
	// public variables
	// -------------------------------------------------------------------------------
	/* NOTHING */

	// -------------------------------------------------------------------------------
	// public methods
	// -------------------------------------------------------------------------------


	// -------------------------------------------------------------------------------
	// @brief	コンストラクタ
	// -------------------------------------------------------------------------------
	ConstantBuffer();

	// -------------------------------------------------------------------------------
	// @brief	デストラクタ
	// -------------------------------------------------------------------------------
	~ConstantBuffer();

	// -------------------------------------------------------------------------------
	// @brief	初期化処理
	// 
	// @param[in]	pDevice		デバイス
	// @param[in]	pPool		ディスクリプタプール
	// @retval	true	初期化に成功
	// @retval	false	初期化に失敗
	// -------------------------------------------------------------------------------
	bool Init(
		ID3D12Device*	_pDevice,
		DescriptorPool* _pPool,
		size_t			_size);

	// -------------------------------------------------------------------------------
	// @brief	終了処理
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	GPU仮想アドレスを取得
	// 
	// @return	GPU仮想アドレスを返却
	// -------------------------------------------------------------------------------
	D3D12_GPU_VIRTUAL_ADDRESS GetAddress() const;

	// -------------------------------------------------------------------------------
	// @brief	CPUディスクリプタハンドルを取得
	// 
	// @return	CPUディスクリプタハンドルを返却
	// -------------------------------------------------------------------------------
	D3D12_CPU_DESCRIPTOR_HANDLE GetHandleCPU() const;

	// -------------------------------------------------------------------------------
	// @brief	GPUディスクリプタハンドルを取得
	// 
	// @return	GPUディスクリプタハンドルを返却
	// -------------------------------------------------------------------------------
	D3D12_GPU_DESCRIPTOR_HANDLE GetHandleGPU() const;

	// -------------------------------------------------------------------------------
	// @brief	メモリマッピング済みポインタを取得
	// 
	// @return	メモリマッピング済みポインタを返却
	// -------------------------------------------------------------------------------
	void* GetPtr() const;

	// -------------------------------------------------------------------------------
	// @brief	メモリマッピング済みポインタを取得
	// 
	// @return	メモリマッピング済みポインタを返却
	// -------------------------------------------------------------------------------
	template<typename T>
	T* GetPtr() 
	{ return reinterpret_cast<T*>(GetPtr()); }

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	ComPtr<ID3D12Resource>			m_pCB;			// 定数バッファ
	DescriptorHandle*				m_pHandle;		// ディスクリプタハンドル
	DescriptorPool*					m_pPool;		// ディスクリプタプール
	D3D12_CONSTANT_BUFFER_VIEW_DESC m_Desc;			// 定数バッファビューの構成設定
	void*							m_pMappedPtr;	// マップ済みポインタ

	// -------------------------------------------------------------------------------
	// private methods
	// -------------------------------------------------------------------------------
	ConstantBuffer	(const ConstantBuffer&) = delete;	// アクセス禁止
	void operator = (const ConstantBuffer&) = delete;	// アクセス禁止


};


