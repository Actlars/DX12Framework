#pragma once

// -------------------------------------------------------------------------------
// 前方宣言
// -------------------------------------------------------------------------------
class RenderQueue;

// -------------------------------------------------------------------------------
// IRenderable インターフェース
// 
// 概要 : 
//	描画コマンドを積むコンポーネントのインターフェース
//	Drawが必要なComponentだけがこれを継承する
// 
//	GameObjectのDrawはこのインターフェースを持つComponentだけを対象に呼び出すため
//	TransformComponentなどの非描画Componentに空実装を行わなくてよい
// 
// 拡張 : 
//	レンダースレッド分離時はDrawの代わりに
//	CollectRenderCommandsでコマンドキューに積む形に変える
// 
// 実装クラス : 
//	MeshComponent		メッシュの描画
//	SpriteComponent		スプライト描画
//	ParticleComponent	パーティクル描画
// -------------------------------------------------------------------------------
class IRenderable
{
public:

	virtual ~IRenderable() = default;

	// -------------------------------------------------------------------------------
	// @brief	描画コマンドを積む
	//			BeginFrameとEndFrameの間に呼ばれる
	// 
	// @param[in]	_pCmd	記録中のコマンドリスト
	// -------------------------------------------------------------------------------
	virtual void Submit(RenderQueue* _pQueue) = 0;

	// -------------------------------------------------------------------------------
	// @brief	描画が有効かどうか返す
	//			falseの場合はGameObjectManagerはDrawを呼ばない
	// -------------------------------------------------------------------------------
	virtual bool IsVisible() const = 0;
};
