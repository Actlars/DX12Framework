#pragma once
// -------------------------------------------------------------------------------
// Forward Declarations
// -------------------------------------------------------------------------------
class GameObject;

// -------------------------------------------------------------------------------
// Component class（基底）
// 
// 概要 : 
//	GameObjectに追加できる機能の基底クラス
//	Updateのみを持ち、描画が必要なコンポーネントは別途IRenderableを継承してDrawを実装する
// 
//	ComponentはGameObjectから生成され、m_pOwnerを通じて親のGameObjectにアクセスできる
//	Component同士は直接参照せず、m_pOwner->GetComponent<T>()を使う
//	（マルチスレッド準備として、コンポーネント同士の直接依存をよける）
// 
//	マルチスレッドについて
//		各ComponentのUpdateはスレッドセーフである必要がある
//		今からUpdate内でグローバル状態を触らないルールを厳守
//		現在はまだ実装していないので関係ない
// 
// 使い方 : 
//	class MyComponent : public Component
//	{
//	public:
//		void Update(float _deltaTime)override{ ... }
//	};
// 
//	auto obj = std::make_unique<GameObject>("Player");
//	obj->AddComponent<MyComponent>();
// -------------------------------------------------------------------------------
class Component
{
public:

	// -------------------------------------------------------------------------------
	// コンストラクタ
	// -------------------------------------------------------------------------------
	Component() = default;

	// -------------------------------------------------------------------------------
	// デストラクタ
	// -------------------------------------------------------------------------------
	virtual ~Component() = default;

	// -------------------------------------------------------------------------------
	// @brief	毎フレームの更新処理（派生クラスが実装）
	// 
	// @param[in]	_deltaTime	前フレームからの経過時間
	// -------------------------------------------------------------------------------
	virtual void Update(float _deltaTime) = 0;

	virtual void OnAttach() {}

	// -------------------------------------------------------------------------------
	// @brief	初期化処理（AddComponent()後に自動で呼ばれる）
	//			派生クラスが必要に応じてオーバーライドする
	// -------------------------------------------------------------------------------
	virtual void OnDetach() {}

	// -------------------------------------------------------------------------------
	// @brief	親のGameObjectを設定する（AddComponent()内部から呼ぶ）
	// -------------------------------------------------------------------------------
	void SetOwner(GameObject* _pOwner) { m_pOwner = _pOwner; }

	// -------------------------------------------------------------------------------
	// @brief	親のGameObjectを返す
	// -------------------------------------------------------------------------------
	GameObject* GetOwner()const { return m_pOwner; }

	// -------------------------------------------------------------------------------
	// @brief	このコンポーネントが有効かどうか返す
	// -------------------------------------------------------------------------------
	bool IsActive() const { return m_IsActive; }

	// -------------------------------------------------------------------------------
	// @brief	このコンポーネントの有効・無効を切り替える
	// -------------------------------------------------------------------------------
	void SetActive(bool _active) { m_IsActive = _active; }

protected:

	GameObject* m_pOwner	= nullptr;		// 親GameObject（所有権なし）
	bool		m_IsActive	= true;			// このコンポーネントが有効かどうか

private:

	Component		(const Component&) = delete;
	void operator = (const Component&) = delete;
};
