// -------------------------------------------------------------------------------
// CameraBase class（純粋仮想基底）
// 
// 概要 : 
//	全カメラの共通インターフェース
//	SceneはCameraBase＊でカメラを持ち、実際の型を知らない
// 
//	派生クラス : 
//		FPSCamera		WASD + マウスで自由移動するFPSカメラ
//		OrbitCamera		注視点を中心に回転するカメラ（将来実装）
//		TPSCamera		ターゲットを追従するTPSカメラ（将来実装）
//		CutCamera		演出用の固定カメラ（将来実装）
// 
// 設計方針 : 
//	- Update()は純粋仮想。派生クラスが毎フレームの行列更新を担当
//  - GetView() / GetProj() / GetPosition() は全カメラ共通のインターフェース
//  - プロジェクション設定（FOV・Aspect・Near・Far）は基底クラスが保持
//	  派生クラスはUpdateProj()を呼ぶことで射影行列を再計算する
// 
// 使い方 : 
//	Scene側
//	std::unique_ptr<CameraBase> m_pCamera;
//	m_pCamera = std::make_unique<CameraBase>();
// 
//	毎フレーム
//	m_pCamera->Update();
//	pCB->View = m_pCamera->GetView();
//	pCB->Proj = m_pCamera->GetProj();
// -------------------------------------------------------------------------------
class CameraBase
{
public:

	// -------------------------------------------------------------------------------
	// コンストラクタ
	// -------------------------------------------------------------------------------
	CameraBase();

	// -------------------------------------------------------------------------------
	// デストラクタ
	// -------------------------------------------------------------------------------
	virtual ~CameraBase();

	// -------------------------------------------------------------------------------
	// @brief	毎フレーム更新処理（派生クラスが実装）
	// -------------------------------------------------------------------------------
	virtual void Update() = 0;

	// -------------------------------------------------------------------------------
	// @brief	ビュー行列を返す
	// -------------------------------------------------------------------------------
	virtual DirectX::XMMATRIX GetView() const = 0;

	// -------------------------------------------------------------------------------
	// @brief	プロジェクション行列を返す
	// -------------------------------------------------------------------------------
	virtual DirectX::XMMATRIX GetProj() const = 0;

	// -------------------------------------------------------------------------------
	// @brief	カメラのワールド座標を返す
	// -------------------------------------------------------------------------------
	virtual DirectX::XMFLOAT3 GetPosition() const = 0;

	// -------------------------------------------------------------------------------
	// プロジェクション設定
	// 
	// 全カメラ共通の設定
	// 変更後に派生クラスがUpdateProj()を呼ぶことで射影行列が再計算される
	// -------------------------------------------------------------------------------

	// @brief	垂直FOVを変更（度単位）
	void SetFov(float _fovDeg);

	// @brief	アスペクト比を変更（ウィンドウリサイズ時に呼ぶ）
	void SetAspect(float _aspect);

	// @brief	ニアー・ファークリップを変更
	void SetNearFar(float _near, float _far);

	// プロジェクションパラメータのゲッター
	float GetFOV()		const	{ return m_Fov; }
	float GetAspect()	const	{ return m_Aspect; }
	float GetNear()		const	{ return m_Near; }
	float GetFar()		const	{ return m_Far; }

protected:

	// -------------------------------------------------------------------------------
	// @brief	プロジェクション行列を再計算
	//			SetFov(), SetAspect, SetNearFarが呼ばれたときに内部で使う
	// -------------------------------------------------------------------------------
	void UpdateProj();

	// -------------------------------------------------------------------------------
	// プロジェクション設定
	// -------------------------------------------------------------------------------
	float m_Fov		= 45.0f;		// 垂直FOV
	float m_Aspect	= 16.0f / 9.0f;	// アスペクト比
	float m_Near	= 0.1f;			// ニアクリップ
	float m_Far		= 1000.0f;		// ファークリップ

	// キャッシュ済みプロジェクション行列
	DirectX::XMMATRIX m_Proj = DirectX::XMMatrixIdentity();

private:

	CameraBase		(const CameraBase&) = delete;
	void operator = (const CameraBase&) = delete;

};


