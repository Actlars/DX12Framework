#pragma once

// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------

// -------------------------------------------------------------------------------
// 前方宣言
// -------------------------------------------------------------------------------
class Fence;

// -------------------------------------------------------------------------------
// CommandList class
// -------------------------------------------------------------------------------
class CommandList
{
public:

	// -------------------------------------------------------------------------------
	// @brief	コンストラクタ
	// -------------------------------------------------------------------------------
	CommandList();

	// -------------------------------------------------------------------------------
	// @brief	デストラクタ
	// -------------------------------------------------------------------------------
	~CommandList();

	// -------------------------------------------------------------------------------
	// @brief	初期化処理
	// 
	// @param[in]	pDevice		デバイス
	// @param[in]	type		コマンドリストタイプ
	// @param[in]	count		アロケータの数、ダブルバッファ化する場合は2
	// @retval	true	初期化に成功
	// @retval	false	初期化に失敗
	// -------------------------------------------------------------------------------
	bool Init(ID3D12Device* _pDevie, D3D12_COMMAND_LIST_TYPE _type, uint32_t _count);

	// -------------------------------------------------------------------------------
	// @brief	終了処理
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	これから使うアロケータがGPUで使用中か_pFenceで確認してからリセットする
	//			必要な時だけ待機する（毎回無条件には待機しない）
	// 
	// @return	リセット処理を行ったコマンドリストを返却
	// -------------------------------------------------------------------------------
	ID3D12GraphicsCommandList* Reset(Fence* _pFence);

	// -------------------------------------------------------------------------------
	// @brief	今使ったアロケータの完了フェンス値を記録し、次のアロケータに切り替える
	//			EndFrameでExecuteCommandListsの直後、Signal()の戻り値を渡して呼ぶ
	// -------------------------------------------------------------------------------
	void RecordFenceValue(UINT64 _value);

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	ComPtr<ID3D12GraphicsCommandList>			m_pCmdList;		// コマンドリスト
	std::vector<ComPtr<ID3D12CommandAllocator>> m_pAllocators;	// コマンドアロケータ
	std::vector<UINT64>							m_FenceValues;	// 各アロケータごとの完了フェンス値
	uint32_t									m_Index;		// アロケータ番号

	// -------------------------------------------------------------------------------
	// private methods
	// -------------------------------------------------------------------------------
	CommandList		(const CommandList&) = delete;
	void operator = (const CommandList&) = delete;
};



