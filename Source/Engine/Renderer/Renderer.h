#pragma once
// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------
#include <Engine/GraphicsDevice/GraphicsDevice.h>

// -------------------------------------------------------------------------------
// Renderer class
// 
// 概要 : 
//	1フレームの描画フロー（BeginFrame → 描画 → EndFrame → Present）を管理する
// 
//	Applicationはこのクラスを通じて描画フレームを制御する
//	シーン固有の描画コマンドはSceneManager::Renderが積む
// 
// 責務の分担 : 
//	GraphicsDevice	デバイス・リソースの生成と管理
//	Renderer		フレームループの制御（バリア・クリア・Present）
//	IScene			シーン固有の描画コマンドの組み立て
// 
// フレームの流れ : 
//	auto* pCmd = renderer.BeginFrame();	バリア・クリア・RTV設定・Viewport設定
//	SceneManager::Render(pCmd);			シーン固有の描画コマンドを積む
//	renderer.EndFrame();				バリア・Close・Execute
//	renderer.Present(1);				スワップ・フェンス待ち・フレーム番号更新
// -------------------------------------------------------------------------------
class Renderer
{
public:

	// -------------------------------------------------------------------------------
	// コンストラクタ
	// -------------------------------------------------------------------------------
	Renderer();

	// -------------------------------------------------------------------------------
	// デストラクタ
	// -------------------------------------------------------------------------------
	~Renderer();

	// -------------------------------------------------------------------------------
	// @brief	初期化
	// 
	// @param[in]	_pGraphicsDevice	GraphicsDeviceへのポインタ
	// @retval	true	成功
	// @retval	false	失敗
	// -------------------------------------------------------------------------------
	bool Init(GraphicsDevice* _pGraphicsDevice);

	// -------------------------------------------------------------------------------
	// @brief	終了処理
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	フレーム開始処理
	// 
	//	内部で行う処理
	//		1. CommandListをリセット
	//		2. バックバッファを PRESENT → RENDER_TARGET に変更
	//		3. ColorTarget / DepthTarget をクリア
	//		4. レンダーターゲットを設定
	//		5. ビューポート・シザー矩形を設定
	// 
	// @return	記録中のコマンドリスト
	// -------------------------------------------------------------------------------
	ID3D12GraphicsCommandList* BeginFrame();

	// -------------------------------------------------------------------------------
	// @brief	フレーム終了処理
	// 
	//	内部で行う処理
	//		1. バックバッファのバリアを RENDER_TARGET → PRESENT に遷移
	//		2. コマンドリストを Close して ExecuteCommandLists
	// -------------------------------------------------------------------------------
	void EndFrame();

	// -------------------------------------------------------------------------------
	// @brief	画面表示とフレームバッファの切り替え
	// 
	//	内部で行う処理
	//		1. SwapChain::Present
	//		2. Fence::Waitで待機
	//		3. フレームインデックスを更新
	// 
	// @param[in]	_syncInterval	垂直同期間隔（0 = 無効、1 = 有効）
	// -------------------------------------------------------------------------------
	void Present(uint32_t _syncInterval);

	// -------------------------------------------------------------------------------
	// @breif	現在のフレームインデックスを返す
	// -------------------------------------------------------------------------------
	uint32_t GetFrameIndex() const;

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	GraphicsDevice*				m_pGraphicsDevice	= nullptr;	// 所有権なし
	ID3D12GraphicsCommandList*	m_pCurrentCmd		= nullptr;	// 現在のフレームのコマンドリスト

	D3D12_VIEWPORT	m_ViewPort	= {};
	D3D12_RECT		m_Scissor	= {};

	static constexpr	float CLEAR_COLOR[4] = { 0.0f,0.0f,1.0f,1.0f };

	Renderer		(const Renderer&) = delete;
	void operator = (const Renderer&) = delete;
};
