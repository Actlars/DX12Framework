#pragma once
// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------
#include <Engine/Scene/IScene.h>
#include "Engine/GraphicsDevice/GraphicsDevice.h"

// -------------------------------------------------------------------------------
// SceneManager class
// 
// 概要 : 
//	シーンのライフサイクルと切り替えを管理するクラス
//	Applicationはこのクラスを通してシーンを操作する
// 
//	遅延切り替え : 
//		ChangeSceneでシーンを予約し、フレーム末のProcessSceneChangeで安全に切り替える
//		描画中にシーンが切り替わることによるGPUリソースのエラーを防ぐ
// 
//	フレームの流れ : 
//		1. Update()				現在のSceneのOnUpdateを呼ぶ
//		2. Render()				現在のSceneのOnRenderを呼ぶ
//		3. ProcessSceneChange()	次のシーンが予約されていれば切り替える
//			現在のSceneのOnTermを呼ぶ
//			GPU完了を待つ
//			次のシーンのOnInitを呼ぶ
//			m_pCurrentSceneを入れ替える
// 
// 使い方 : 
//	SceneManager sceneManager;
//	SceneManager.Init(&graphicsDevice);
//	SceneManager.ChangeScene(std::make_unique<GameScene>());
// 
//	メインループ
//	SceneManager::Update(deltaTime);
//	auto *pCmd = graphicsDevice.BeginFrame();
//	SceneManager.Render(pCmd);
//	graphicsDevice.EndFrame();
//	graphicsDevice.Present(1);
//	SceneManager.ProcessSceneChange();	//フレーム末に切り替え処理
// -------------------------------------------------------------------------------
class SceneManager
{
public:

	// -------------------------------------------------------------------------------
	// コンストラクタ
	// -------------------------------------------------------------------------------
	SceneManager();

	// -------------------------------------------------------------------------------
	// デストラクタ
	// -------------------------------------------------------------------------------
	~SceneManager();

	// -------------------------------------------------------------------------------
	// @brief	初期化
	//			GraphicsDeviceへの参照を保持する（所有権なし）
	// 
	// @param[in]	_pGraphicsDevice	GraphicsDeviceへのポインタ
	// -------------------------------------------------------------------------------
	void Init(GraphicsDevice* _pGrahicsDevice);

	// -------------------------------------------------------------------------------
	// @brief	終了処理	現在のシーンを終了させて解放する
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	次のシーンを予約する（遅延切り替え）
	//			実際の切り替えはProcessSceneChangeで行う
	// 
	// @param[in]	_pScene		次のシーン（unique_ptrで所有権を移転）
	// -------------------------------------------------------------------------------
	void ChangeScene(std::unique_ptr<IScene> _pScene);

	// -------------------------------------------------------------------------------
	// @brief	現在のシーンを更新する
	//			メインループのBeginFrameより前に呼ぶ
	// 
	// @param[in]	_deltaTime		前フレームからの経過時間
	// -------------------------------------------------------------------------------
	void Update(float _deltaTime);

	// -------------------------------------------------------------------------------
	// @brief	現在のシーンの描画コマンドを積む
	//			BeginFrameとEndFrameの間に呼ぶ
	// 
	// @param[in]	_pCmd	記録中のコマンドリスト
	// -------------------------------------------------------------------------------
	void Render(ID3D12GraphicsCommandList* _pCmd);

	// -------------------------------------------------------------------------------
	// @brief	シーンの切り替え処理
	//			Presetの後、次フレームの先頭より前に呼ぶ
	//			次のシーンが予約されていなければ何もしない
	// -------------------------------------------------------------------------------
	void ProcessSceneChange();

	// -------------------------------------------------------------------------------
	// @brief	現在のシーンを返す（デバッグ用）
	// -------------------------------------------------------------------------------
	IScene* GetCurrentScene() const;

	// -------------------------------------------------------------------------------
	// @brief	シーン切り替えが予約されているかを返す
	// -------------------------------------------------------------------------------
	bool HasPendingScene() const;

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	GraphicsDevice*			m_pGraphicsDevice = nullptr;	// 所有権なし
	std::unique_ptr<IScene> m_pCurrentScene;				// 現在のシーン
	std::unique_ptr<IScene> m_pNextScene;					// 予約済みの次のシーン

	SceneManager	(const SceneManager&) = delete;
	void operator = (const SceneManager&) = delete;

};
