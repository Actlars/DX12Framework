#pragma once
// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------
#include <Engine/GraphicsDevice/GraphicsDevice.h>

// -------------------------------------------------------------------------------
// IScene インターフェース（純粋仮想基底）
// 
// 概要 : 
//	全シーン共通のインターフェース
//	SceneManagerはIScene*でシーンを持ち、実際の型は知らない
// 
//	RootSignatureとPSOはシーンごとに異なるシェーダーを使えるようISceneが持つ
//	基底クラスでInitRootSignatureとInitPipelineStateの骨格を提供し、派生クラスがオーバーライドして実装
// 
// 派生クラス : 
//	GameScene	通常のゲームシーン
//	TitleScene	タイトル画面（将来）
// 
// 使い方 : 
//	SceneManager経由で使う（直接使わない）
//	SceneManager.ChangeScene(std::make_unique<GameScene>());
// -------------------------------------------------------------------------------
class IScene
{
public :

	// -------------------------------------------------------------------------------
	// コンストラクタ / デストラクタ
	// -------------------------------------------------------------------------------
	IScene()			= default;
	virtual ~IScene()	= default;

	// -------------------------------------------------------------------------------
	//	@brief	シーンの初期化
	// 
	// @param[in]	_pGraphicsDevice	GraphicsDeviceへの参照（所有権なし）
	// @retval	true	初期化成功
	// @retval	false	初期化失敗
	// -------------------------------------------------------------------------------
	virtual bool OnInit(GraphicsDevice* _pGraphicsDevice) = 0;

	// -------------------------------------------------------------------------------
	// @brief	シーンの終了
	//			SceneManagerからシーンの切り替え時・終了時に呼ばれる
	//			GPU完了待ちはSceneManager側で行い、ここではGPUリソースの解放を行う
	// -------------------------------------------------------------------------------
	virtual void OnTerm() = 0;

	// -------------------------------------------------------------------------------
	// @brief	毎フレームの更新
	//			入力処理・オブジェクトの更新・カメラ更新などを行う
	// 
	// @param[in]	_deltaTime	前フレームからの経過時間
	// -------------------------------------------------------------------------------
	virtual void OnUpdate(float _deltaTime) = 0;

	// -------------------------------------------------------------------------------
	// @brief	毎フレームの描画処理の組み立て
	//			BeginFrameとEndFrameの間に呼ばれる
	//			RootSignature・PSO・CB・テクスチャのバインドとDraw命令を積む
	// 
	// @param[in]	_pCmd	記録中のコマンドリスト
	// -------------------------------------------------------------------------------
	virtual void OnRender(ID3D12GraphicsCommandList* _pCmd) = 0;

	// -------------------------------------------------------------------------------
	// @brief	初期化済みかどうかを返す
	// -------------------------------------------------------------------------------
	bool IsInitialized() const { return m_IsInitialized; }

protected:

	// -------------------------------------------------------------------------------
	// @brief	RootSignatureを生成する（派生クラスがOnInit()内で呼ぶ）
	// 
	// @param[in]	_pDevice	デバイス
	// @retval	true	成功
	// @retval	false	失敗
	// -------------------------------------------------------------------------------
	virtual bool InitRootSignature(ID3D12Device* _pDevice) = 0;

	// -------------------------------------------------------------------------------
	// @brief	PSOを生成する（派生クラスがOnInit()内で呼ぶ）
	// 
	// @param[in]	_pDevice	デバイス
	// @retval	true	成功
	// @retval	false	失敗
	// -------------------------------------------------------------------------------
	virtual bool InitPipelineState(ID3D12Device* _pDevice) = 0;

	// -------------------------------------------------------------------------------
	// 派生クラスから参照できる共通リソース
	// -------------------------------------------------------------------------------
	GraphicsDevice*				m_pGraphicsDevice	= nullptr;	// 所有権なし
	ComPtr<ID3D12RootSignature> m_pRootSignature;				// シーン固有
	ComPtr<ID3D12PipelineState> m_pPSO;							// シーン固有
	bool						m_IsInitialized		= false;	

private:

	IScene			(const IScene&) = delete;
	void operator = (const IScene&) = delete;
};
