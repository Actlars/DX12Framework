#include "../Pool.h"
#include "../../Utility/ComPtr/ComPtr.h"

// -------------------------------------------------------------------------------
// DescriptorHandle class
// -------------------------------------------------------------------------------
class DescriptorHandle
{
public:

	D3D12_CPU_DESCRIPTOR_HANDLE HandleCPU;	// CPUディスクリプタハンドル
	D3D12_GPU_DESCRIPTOR_HANDLE HandleGPU;	// GPUディスクリプタハンドル

	bool HasCPU() const 
	{ return HandleCPU.ptr != 0; }

	bool HasGPU() const 
	{ return HandleGPU.ptr != 0; }

};

// -------------------------------------------------------------------------------
// DescriptorPool class
// -------------------------------------------------------------------------------
class DescriptorPool
{
	// -------------------------------------------------------------------------------
	// list of friend classes and methods
	// -------------------------------------------------------------------------------
	/* NOTHING */

public:

	// -------------------------------------------------------------------------------
	// public variables
	// -------------------------------------------------------------------------------
	/* NOTHING */

	// -------------------------------------------------------------------------------
	// public methods
	// -------------------------------------------------------------------------------

	// -------------------------------------------------------------------------------
	// @brief	生成処理
	// 
	// @param[in]	pDevice	デバイス
	// @param[in]	pDesc	ディスクリプタヒープの構成設定
	// @param[in]	ppPool	ディスクリプタプールの格納先
	// @retval	true	生成処理に成功
	// @retval	false	生成処理に失敗
	// -------------------------------------------------------------------------------
	static bool Create(
		ID3D12Device*						_pDevice,
		const D3D12_DESCRIPTOR_HEAP_DESC*	_pDesc,
		DescriptorPool**					_ppPool);

	// -------------------------------------------------------------------------------
	// @brief	参照カウントを増やす
	// -------------------------------------------------------------------------------
	void AddRef();

	// -------------------------------------------------------------------------------
	// @brief	解放処理を行う
	// -------------------------------------------------------------------------------
	void Release();

	// -------------------------------------------------------------------------------
	// @brief	参照カウントを取得
	// 
	// @return	参照カウントを返却
	// -------------------------------------------------------------------------------
	uint32_t GetCount() const;

	// -------------------------------------------------------------------------------
	// @brief	ディスクリプタハンドルを割り当て
	// 
	// @return	割り当てられたディスクリプタハンドルを返却
	// -------------------------------------------------------------------------------
	DescriptorHandle* AllocHandle();

	// -------------------------------------------------------------------------------
	// @brief	ディスクリプタハンドルを開放
	// 
	// @param[in]	pHandle	解放するハンドルへのポインタ
	// -------------------------------------------------------------------------------
	void FreeHandle(DescriptorHandle*& _pHandle);

	// -------------------------------------------------------------------------------
	// @brief	利用可能なハンドル数を取得
	// 
	// @return	利用可能なハンドル数を返却
	// -------------------------------------------------------------------------------
	uint32_t GetAvailableHandleCount() const;

	// -------------------------------------------------------------------------------
	// @brief	割り当て済みのハンドル数を取得
	// 
	// @return	割り当て済みのハンドル数を返却
	// -------------------------------------------------------------------------------
	uint32_t GetAllocatedHandleCount() const;

	// -------------------------------------------------------------------------------
	// @brief	ハンドル総数を取得
	// 
	// @return	ハンドル総数を返却
	// -------------------------------------------------------------------------------
	uint32_t GetHandleCount() const;

	// -------------------------------------------------------------------------------
	// @brief	ディスクリプタヒープを取得
	// 
	// @return	ディスクリプタヒープを返却
	// -------------------------------------------------------------------------------
	ID3D12DescriptorHeap* const GetHeap() const;

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	std::atomic<uint32_t>			m_RefCount;			// 参照カウント
	Pool<DescriptorHandle>			m_Pool;				// ディスクリプタハンドルプール
	ComPtr<ID3D12DescriptorHeap>	m_pHeap;			// ディスクリプタヒープ
	uint32_t						m_DescriptorSize;	// ディスクリプタサイズ

	// -------------------------------------------------------------------------------
	// private methods
	// -------------------------------------------------------------------------------


	// -------------------------------------------------------------------------------
	// @brief	コンストラクタ
	// -------------------------------------------------------------------------------
	DescriptorPool();

	// -------------------------------------------------------------------------------
	// @brief	デストラクタ
	// -------------------------------------------------------------------------------
	~DescriptorPool();

	DescriptorPool	(const DescriptorPool&) = delete;	// アクセス禁止
	void operator = (const DescriptorPool&) = delete;	// アクセス禁止

};



