#pragma once

// -------------------------------------------------------------------------------
// Includes
// -------------------------------------------------------------------------------

// -------------------------------------------------------------------------------
// Fence class
// -------------------------------------------------------------------------------
class Fence
{
public:

	// -------------------------------------------------------------------------------
	// @brief	コンストラクタ
	// -------------------------------------------------------------------------------
	Fence();

	// -------------------------------------------------------------------------------
	// @brief	デストラクタ
	// -------------------------------------------------------------------------------
	~Fence();

	// -------------------------------------------------------------------------------
	// @brief	初期化処理
	// 
	// @param[in]	pDevice		デバイス
	// @retval	true	初期化に成功
	// @retval	false	初期化に失敗
	// -------------------------------------------------------------------------------
	bool Init(ID3D12Device* _pDevice);

	// -------------------------------------------------------------------------------
	// @brief	終了処理を行う
	// -------------------------------------------------------------------------------
	void Term();

	// -------------------------------------------------------------------------------
	// @brief	シグナル状態になるまで指定された時間待機
	// 
	// @param[in]	pQueue		コマンドキュー
	// @param[in]	timeout		タイムアウト時間（ミリ秒）
	// -------------------------------------------------------------------------------
	void Wait(ID3D12CommandQueue* _pQueue, UINT _timeout);

	// -------------------------------------------------------------------------------
	// @brief	シグナル状態になるまでずっと待機
	// 
	// @param[in]	pQueue		コマンドキュー
	// -------------------------------------------------------------------------------
	void Sync(ID3D12CommandQueue* _pQueue);

private:

	// -------------------------------------------------------------------------------
	// private variables
	// -------------------------------------------------------------------------------
	ComPtr<ID3D12Fence> m_pFence;	// フェンス
	HANDLE				m_Event;	// イベント
	UINT				m_Counter;	// 現在のカウンター

	// -------------------------------------------------------------------------------
	// private methods
	// -------------------------------------------------------------------------------
	Fence			(const Fence&) = delete;
	void operator = (const Fence&) = delete;
};
