#pragma once

// -------------------------------------------------------------------------------
// MeshResource class （純粋仮想基底）
// 
// 概要 : 
//	「メッシュの描画リソース」の基底インターフェース
//	従来のVertexBuffer/IndexBufferを使うPolygonMeshResourceや
//	将来のメッシュシェーダー用MeshletResourceなど
//	描画バックエンドの違いをこのインターフェースで吸収する
// 
//  Meshクラスは子のポインタだけを持ち
//	実際の描画方法（IA経由かDispathMeshか）を意識しない
// 
// 派生クラス : 
//	PolygonMeshResource	従来パイプライン（VB/IB)
//	MeshletResource		メッシュシェーダーパイプライン（将来実装）
// -------------------------------------------------------------------------------
class MeshResource
{
public:

	virtual ~MeshResource() = default;

	// -------------------------------------------------------------------------------
	// @brief	描画コマンドを積む
	// 
	//	派生クラスが適切な描画命令（DrawIndexedInstanced / DispathMesh等）を
	//	コマンドリストに積む。呼び出し側は描画方法を知らなくてよい
	// 
	// @param[in]	_pCmd			コマンドリスト
	// @param[in]	_instanceCount	インスタンス数は（通常は1）
	// -------------------------------------------------------------------------------

	virtual void Draw(
		ID3D12GraphicsCommandList*	_pCmd,
		uint32_t					_instanceCount = 1) = 0;

	// -------------------------------------------------------------------------------
	// @brief	InputLayoutが必要かどうかを返す
	// 
	//	従来のパイプライン → true（PSOにInputLayoutを設定する必要がある）
	//	メッシュシェーダー → false（InputLayout不要）
	// 
	// @return true : InputLayout が必要
	// -------------------------------------------------------------------------------
	virtual bool NeedsInputLayout() const = 0;

	// -------------------------------------------------------------------------------
	// @brief	インデックス数を返す（DrawIndexedInstancedの引数等に使う）
	// -------------------------------------------------------------------------------
	virtual uint32_t GetIndexCount() const = 0;

	// -------------------------------------------------------------------------------
	// @brief	頂点数を返す
	// -------------------------------------------------------------------------------
	virtual uint32_t GetVertexCount() const = 0;
};
